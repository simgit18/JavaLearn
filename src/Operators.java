public class Operators {
    public static void main(String[] args) {
        int a=16, b=5;

        int result = a + b;
        System.out.println("Addition:" +result);

        result = a * b;
        System.out.println("Multiplication:"+result);

        result = a/b;
        System.out.println("Division:"+result);

        result = a%b;
        System.out.println("Modulas:" + result);



    }
}
